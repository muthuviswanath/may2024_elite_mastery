
public class PrintRanges {
	public static void main(String[] args) {
		System.out.println("byte ranges from: "+Byte.MIN_VALUE + " to " + Byte.MAX_VALUE);
		System.out.println("short ranges from: "+Short.MIN_VALUE + " to " + Short.MAX_VALUE);
		System.out.println("int ranges from: "+Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);
		System.out.println("long ranges from: "+Long.MIN_VALUE + " to " + Long.MAX_VALUE);
		System.out.println("float ranges from: "+Float.MIN_VALUE + " to " + Float.MAX_VALUE);
		System.out.println("double ranges from: "+Double.MIN_VALUE + " to " + Double.MAX_VALUE);
		System.out.println("char ranges from: "+Character.MIN_VALUE + " to " + Character.MAX_VALUE);
		
	}
}
